# 重複除去・無効除去後のクリーンなリスト
def get_nikkei225_tickers() -> list[str]:
    """有効な日経225ティッカーリストを返す。"""
    seen = set()
    result = []
    for t in NIKKEI225_TICKERS:
        # 不正形式（数字4桁.Tでないもの）は除外
        parts = t.split(".")
        if len(parts) == 2 and parts[1] == "T" and parts[0].isdigit() and len(parts[0]) == 4:
            if t not in seen:
                seen.add(t)
                result.append(t)
    return result


